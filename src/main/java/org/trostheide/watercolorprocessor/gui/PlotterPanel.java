    private void selectFile() {
        JFileChooser fc = new JFileChooser();
        File current = new File(jsonField.getText());
        if (current.exists())
            fc.setSelectedFile(current);
        else
            fc.setCurrentDirectory(new File("."));

        if (fc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            jsonField.setText(fc.getSelectedFile().getAbsolutePath());
            visPanel.loadFromJson(fc.getSelectedFile());
        }
    }

    private void startProcess() {
        String jsonPath = jsonField.getText().trim();
        if (jsonPath.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a JSON file.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        File jsonFile = new File(jsonPath);
        if (!jsonFile.exists()) {
            JOptionPane.showMessageDialog(this, "File not found: " + jsonPath, "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Auto-save config so driver sees latest changes
        settingsPanel.saveConfigSilent();

        // Apply visual overlay transform to JSON if user has dragged/resized
        boolean overlayApplied = false;
        if (visPanel.hasOverlayTransform()) {
            try {
                jsonFile = applyOverlayToJson(jsonFile,
                        visPanel.getOverlayScale(), visPanel.getOverlayOffsetX(), visPanel.getOverlayOffsetY(),
                        visPanel);
                jsonField.setText(jsonFile.getAbsolutePath());
                appendToConsole("Applied visual transform: scale=" +
                        String.format("%.2f", visPanel.getOverlayScale()) +
                        " offset=(" + String.format("%.1f, %.1f", visPanel.getOverlayOffsetX(), visPanel.getOverlayOffsetY()) + ")");
                overlayApplied = true;
                visPanel.resetOverlay();
            } catch (Exception ex) {
                appendToConsole("Warning: could not apply visual transform: " + ex.getMessage());
            }
        }

        // Reload vis with the (possibly baked) JSON
        visPanel.loadFromJson(jsonFile);
        updateVisualSettings();
        if (overlayApplied) {
            visPanel.setSuppressAlignment(true);
        }

        List<String> cmd = buildDriverCommand();
        cmd.add(2, jsonFile.getAbsolutePath()); // insert after driver.py path

        if (debugPositionCheckBox.isSelected()) {
            cmd.add("--debug-position");
        }

        // Skip alignment when user manually positioned content — the baked coords
        // already encode the final motor-space position
        if (!overlayApplied) {
            boolean isPortrait = settingsPanel.isPortrait();
            boolean needsAxisSwap = isPortrait && settingsPanel.getMachineWidth() > settingsPanel.getMachineHeight();
            String alignment = settingsPanel.getCanvasAlignment();
            if (alignment != null && !alignment.isEmpty()) {
                if (needsAxisSwap) {
                    alignment = translateAlignmentForPortrait(alignment,
                            settingsPanel.isOriginRight(), settingsPanel.isOriginBottom());
                }
                cmd.add("--canvas-align");
                cmd.add(alignment.toLowerCase().replace(" ", "-"));
            }

            cmd.add("--padding-x");
            cmd.add(String.valueOf(settingsPanel.getPaddingX()));
            cmd.add("--padding-y");
            cmd.add(String.valueOf(settingsPanel.getPaddingY()));
        }

        int rotation = settingsPanel.getViewRotation();
        if (rotation != 0) {
            cmd.add("--data-rotation");
            cmd.add(String.valueOf(rotation));
        }

        cmd.add("--report-position");

        consoleArea.setText("");
        appendToConsole("Starting driver...");
        appendToConsole("Command: " + String.join(" ", cmd));

        ProcessBuilder pb = new ProcessBuilder(cmd);
        pb.redirectErrorStream(true);

        try {
            currentProcess = pb.start();
            processInputWriter = new BufferedWriter(new OutputStreamWriter(currentProcess.getOutputStream()));

            startButton.setEnabled(false);
            stopButton.setEnabled(true);
            inputButton.setEnabled(true);

            settingsPanel.setSettingsEnabled(false);

            new Thread(() -> {
                try (BufferedReader reader = new BufferedReader(
                        new InputStreamReader(currentProcess.getInputStream()))) {
                    String line;
                    while ((line = reader.readLine()) != null) {
                        final String l = line;

                        if (l.startsWith("POS:")) {
                            try {
                                String[] parts = l.split(":");
                                double x = Double.parseDouble(parts[2]);
                                double y = Double.parseDouble(parts[4]);
                                SwingUtilities.invokeLater(() -> visPanel.updatePosition(x, y));
                            } catch (Exception parseEx) {
                                // ignore parse errors
                            }
                            continue;
                        }

                        if (l.contains("INFO: Connection Successful")) {
                            updateStatus(true);
                        } else if (l.contains("ERROR: Could not connect") || l.contains("Connection Failed")) {
                            updateStatus(false);
                        }

                        SwingUtilities.invokeLater(() -> appendToConsole(l));
                    }
                } catch (IOException e) {
                    if (currentProcess != null && currentProcess.isAlive()) {
                        e.printStackTrace();
                    }
                } finally {
                    SwingUtilities.invokeLater(() -> {
                        appendToConsole("--- Process Exited ---");
                        updateStatus(false);
                        processCleanup();
                    });
                }
            }).start();

        } catch (IOException e) {
            appendToConsole("Error starting process: " + e.getMessage());
            e.printStackTrace();
        }
    }

    private void stopProcess() {
        if (currentProcess != null && currentProcess.isAlive()) {
            currentProcess.destroy();
            appendToConsole("--- Process Killed by User ---");
        }
        processCleanup();
    }

    private void sendInput(String input) {
        if (currentProcess != null && currentProcess.isAlive() && processInputWriter != null) {
            try {
                processInputWriter.write(input);
                processInputWriter.flush();
            } catch (IOException e) {
                appendToConsole("Error sending input: " + e.getMessage());
            }
        }
    }

    private void processCleanup() {
        startButton.setEnabled(true);
        stopButton.setEnabled(false);
        inputButton.setEnabled(false);

        settingsPanel.setSettingsEnabled(true);

        currentProcess = null;
        processInputWriter = null;
    }

    private Process manualServerProcess;
    private BufferedWriter manualServerWriter;

    private List<String> buildDriverCommand() {
        List<String> cmd = new ArrayList<>();
        cmd.add(settingsPanel.getPythonPath());
        cmd.add("driver/driver.py");

        boolean isGcode = "gcode".equals(settingsPanel.getBackend());
        if (isGcode) {
            cmd.add("--backend");
            cmd.add("gcode");
            cmd.add("--serial-port");
            cmd.add(settingsPanel.getSerialPort());
            cmd.add("--machine-width");
            cmd.add(String.valueOf(settingsPanel.getMachineWidth()));
            cmd.add("--machine-height");
            cmd.add(String.valueOf(settingsPanel.getMachineHeight()));
        }

        if (settingsPanel.isMockMode())
            cmd.add("--mock");

        cmd.add("--machine-origin");
        cmd.add(settingsPanel.getMachineOrigin().toLowerCase().replace(" ", "-"));

        boolean isPortrait = settingsPanel.isPortrait();
        boolean needsAxisSwap = isPortrait && settingsPanel.getMachineWidth() > settingsPanel.getMachineHeight();
        if (needsAxisSwap)
            cmd.add("--portrait");
        if (settingsPanel.isFlipY())
            cmd.add("--flip-y");
        if (settingsPanel.isSwapXY())
            cmd.add("--swap-xy");
        if (verboseCheckBox.isSelected())
            cmd.add("--verbose");

        if (!isGcode) {
            cmd.add("--model");
            cmd.add(String.valueOf(settingsPanel.getPlotterModelIndex() + 1));
            cmd.add("--speed-down");
            cmd.add(String.valueOf(settingsPanel.getDrawSpeed()));
            cmd.add("--speed-up");
            cmd.add(String.valueOf(settingsPanel.getTravelSpeed()));
            cmd.add("--pen-up");
            cmd.add(String.valueOf(settingsPanel.getPenUpHeight()));
            cmd.add("--pen-down");
            cmd.add(String.valueOf(settingsPanel.getPenDownHeight()));
        }

        cmd.add("--config");
        cmd.add(settingsPanel.getCurrentConfigFile().getAbsolutePath());

        return cmd;
    }

    private void ensureManualServer() {
        if (manualServerProcess != null && manualServerProcess.isAlive()) {
            return;
        }

        settingsPanel.saveConfigSilent();

        try {
            List<String> cmd = buildDriverCommand();
            cmd.add("--interactive-server");

            appendToConsole("Starting Manual Control Server...");
            ProcessBuilder pb = new ProcessBuilder(cmd);
            pb.redirectErrorStream(true);
            manualServerProcess = pb.start();
            manualServerWriter = new BufferedWriter(new OutputStreamWriter(manualServerProcess.getOutputStream()));

            new Thread(() -> {
                try (BufferedReader reader = new BufferedReader(
                        new InputStreamReader(manualServerProcess.getInputStream()))) {
                    String line;
                    while ((line = reader.readLine()) != null) {
                        final String l = line;

                        if (l.contains("INFO: Connection Successful")) {
                            updateStatus(true);
                        } else if (l.contains("ERROR: Could not connect")) {
                            updateStatus(false);
                        }

                        SwingUtilities.invokeLater(() -> appendToConsole("[SRV] " + l));
                    }
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }).start();

        } catch (IOException e) {
            e.printStackTrace();
            appendToConsole("Failed to start server: " + e.getMessage());
        }
    }

    private void sendServerCommand(String cmd) {
        if (manualServerProcess != null && manualServerProcess.isAlive()) {
            try {
                manualServerWriter.write(cmd + "\n");
                manualServerWriter.flush();
            } catch (IOException e) {
                appendToConsole("Error sending to server: " + e.getMessage());
            }
        }
    }

    private void resetManualServer() {
        if (manualServerProcess != null && manualServerProcess.isAlive()) {
            manualServerProcess.destroy();
            manualServerProcess = null;
            appendToConsole("[SRV] Server reset due to configuration change.");
        }
    }

    private void runManualMove(double dx, double dy) {
        ensureManualServer();
        sendServerCommand(String.format(java.util.Locale.US, "MOVE %f %f", dx, dy));
    }

    private void runManualPen(String direction) {
        ensureManualServer();
        sendServerCommand("PEN " + direction);
    }

    private static String translateAlignmentForPortrait(String label, boolean originRight, boolean originBottom) {
        boolean xor = originRight ^ originBottom;
        if (xor) {
            if ("Top Left".equals(label)) return "Bottom Right";
            if ("Bottom Right".equals(label)) return "Top Left";
        } else {
            if ("Top Right".equals(label)) return "Bottom Left";
            if ("Bottom Left".equals(label)) return "Top Right";
        }
        return label;
    }

    public void loadJsonFile(File file) {
        jsonField.setText(file.getAbsolutePath());
        visPanel.loadFromJson(file);
        updateVisualSettings();
    }

    private void updateSettingsSummary() {
        String backend = "gcode".equals(settingsPanel.getBackend()) ? "GRBL" : "AxiDraw";
        String port = "gcode".equals(settingsPanel.getBackend())
                ? " | " + settingsPanel.getSerialPort() : "";
        settingsSummaryLabel.setText(String.format(
                "%s%s | %s | %.0f x %.0f mm | Align: %s | %s",
                backend, port,
                settingsPanel.getMachineOrigin(),
                settingsPanel.getMachineWidth(), settingsPanel.getMachineHeight(),
                settingsPanel.getCanvasAlignment(),
                settingsPanel.getOrientation()));
    }

    private void updateVisualSettings() {
        updateSettingsSummary();
        visPanel.setMachineSize(settingsPanel.getMachineWidth(), settingsPanel.getMachineHeight());
        visPanel.setMachineOrigin(settingsPanel.getMachineOrigin());

        visPanel.setDataInvertX(settingsPanel.isInvertX());
        visPanel.setDataInvertY(settingsPanel.isInvertY());
        visPanel.setFlipY(settingsPanel.isFlipY());

        visPanel.setSwapXY(settingsPanel.isSwapXY());
        visPanel.setCanvasAlignment(settingsPanel.getCanvasAlignment());
        visPanel.setOrientation(settingsPanel.getOrientation());
        visPanel.setViewRotation(settingsPanel.getViewRotation());
        visPanel.setPadding(settingsPanel.getPaddingX(), settingsPanel.getPaddingY());

        List<VisualizationPanel.Station> visualStations = new ArrayList<>();
        Map<String, StationConfig> stations = settingsPanel.getStations();
        if (stations != null) {
            for (Map.Entry<String, StationConfig> entry : stations.entrySet()) {
                StationConfig cfg = entry.getValue();
                visualStations.add(new VisualizationPanel.Station(entry.getKey(), cfg.x(), cfg.y()));
            }
        }
        visPanel.setStations(visualStations);
    }

